
package entities.models;


public class Localidade {
    private Double s;
    private Double w;

    public Localidade(Double s, Double w) {
        this.s = s;
        this.w = w;
    }

    public Double getS() {
        return s;
    }

    public void setS(Double s) {
        this.s = s;
    }

    public Double getW() {
        return w;
    }

    public void setW(Double w) {
        this.w = w;
    }
    
}
